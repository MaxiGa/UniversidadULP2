    private void jBguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBguardarActionPerformed
       
      
        int dni = Integer.parseInt(jTdocumento.getText().toString());
        String apellido = jTapellido.getText();
        String nombre = jTnombre.getText();
        boolean active = jrActiv.isSelected();
        LocalDate fechaNac = jdFechaN.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        
        Alumno alum = new Alumno(dni, apellido, nombre, fechaNac, active);
        AlumnoData alumno = new AlumnoData();
        alumno.guardarAlumno(alum);
        
        resetearCampos();
        
     
    }//GEN-LAST:event_jBguardarActionPerformed

